                    <h2 id="attributes-POSTapi-admin-attributes">Create an Attribute</h2>

<p>
<small class="badge badge-darkred">requires authentication</small>
</p>

<p>This endpoint allows creating a new attribute.</p>

<span id="example-requests-POSTapi-admin-attributes">
<blockquote>Example request:</blockquote>


<div class="bash-example">
    <pre><code class="language-bash">curl --request POST \
    "http://localhost:8000/api/admin/attributes" \
    --header "Authorization: Bearer {YOUR_AUTH_KEY}" \
    --header "Content-Type: application/json" \
    --header "Accept: application/json" \
    --data "{
    \"name\": \"Title\",
    \"slug\": \"title\",
    \"data_type\": \"string\",
    \"is_required\": true,
    \"is_unique\": true,
    \"default_value\": \"Default Title\"
}"
</code></pre></div>


<div class="php-example">
    <pre><code class="language-php">$client = new \GuzzleHttp\Client();
$url = 'http://localhost:8000/api/admin/attributes';
$response = $client-&gt;post(
    $url,
    [
        'headers' =&gt; [
            'Authorization' =&gt; 'Bearer {YOUR_AUTH_KEY}',
            'Content-Type' =&gt; 'application/json',
            'Accept' =&gt; 'application/json',
        ],
        'json' =&gt; [
            'name' =&gt; 'Title',
            'slug' =&gt; 'title',
            'data_type' =&gt; 'string',
            'is_required' =&gt; true,
            'is_unique' =&gt; true,
            'default_value' =&gt; 'Default Title',
        ],
    ]
);
$body = $response-&gt;getBody();
print_r(json_decode((string) $body));</code></pre></div>


<div class="python-example">
    <pre><code class="language-python">import requests
import json

url = 'http://localhost:8000/api/admin/attributes'
